String detectStateLanguage(double lat, double lng) {
  if (lat >= 11.5 && lat <= 18.5 && lng >= 74.0 && lng <= 78.5) return 'kn'; // Karnataka
  if (lat >= 8.0 && lat <= 12.8 && lng >= 74.5 && lng <= 77.5) return 'ml';  // Kerala
  if (lat >= 8.0 && lat <= 13.5 && lng >= 76.5 && lng <= 80.5) return 'ta';  // Tamil Nadu
  if (lat >= 15.5 && lat <= 22.0 && lng >= 72.5 && lng <= 80.5) return 'mr'; // Maharashtra
  return 'hi'; // fallback
}

